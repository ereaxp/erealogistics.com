const START_B = 104;
const STOP = [2,3,3,1,1,1,2];

export function encodeCode128B(text: string): number[] {
  const values = Array.from(text, (c) => {
    const v = c.charCodeAt(0) - 32;
    if (v < 0 || v > 94) return 0; // clamp non-encodable chars to space
    return v;
  });
  const checksum = values.reduce((sum, v, i) => sum + v * (i + 1), START_B) % 103;
  return [
    ...PATTERNS[START_B],
    ...values.flatMap((v) => PATTERNS[v]),
    ...PATTERNS[checksum],
    ...STOP,
  ];
}

export function encodeCode128Bars(text: string): { width: number; black: boolean }[] {
  return encodeCode128B(text).map((width, i) => ({ width, black: i % 2 === 0 }));
}

export type PositionedBar = { x: number; width: number; black: boolean };

export function encodeCode128Positioned(text: string): { bars: PositionedBar[]; totalWidth: number } {
  let x = 0;
  const bars = encodeCode128Bars(text).map(b => {
    const entry: PositionedBar = { ...b, x };
    x += b.width;
    return entry;
  });
  return { bars, totalWidth: x };
}
